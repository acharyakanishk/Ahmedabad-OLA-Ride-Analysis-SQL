-- 🚕 9. Repeat Customers

-- Find customers who booked more than 3 rides.

-- Good for retention analysis.
SELECT
	CUSTOMER_ID,
	COUNT(BOOKING_ID) AS TOTAL_RIDES
FROM
	AHMEDABAD_RIDES
GROUP BY
	CUSTOMER_ID
HAVING
	COUNT(BOOKING_ID) > 3
ORDER BY
	TOTAL_RIDES DESC;
