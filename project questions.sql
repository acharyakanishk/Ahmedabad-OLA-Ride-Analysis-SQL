--Basic SQL analysis

-- 1. Retrieve all successful bookings:
SELECT
	*
FROM
	AHMEDABAD_RIDES
WHERE
	BOOKING_STATUS = 'Success';
-- 2. Find the average ride distance for each vehicle type:
SELECT
	VEHICLE_TYPE,
	AVG(RIDE_DISTANCE) AS AVG_DISTANCE
FROM
	AHMEDABAD_RIDES
GROUP BY
	VEHICLE_TYPE;
-- 3. Get the total number of cancelled rides by customers:
SELECT
	COUNT(*) AS CUSTOMER_CANCELLED_RIDES
FROM
	AHMEDABAD_RIDES
WHERE
	BOOKING_STATUS = 'Cancelled by Customer';
-- 4. List the top 5 customers who booked the highest number of rides:
SELECT
	COUNT(*) AS TOTAL_RIDES,
	CUSTOMER_ID
FROM
	AHMEDABAD_RIDES
GROUP BY
	CUSTOMER_ID
ORDER BY
	TOTAL_RIDES DESC
LIMIT
	5;
-- 5. Get the number of rides cancelled by drivers due to personal and car-related issues:
SELECT
	COUNT(*) AS Driver_CANCELLED_RIDES
FROM
	AHMEDABAD_RIDES
WHERE
	Driver_cancel_reason = 'Persol & Car related issues';

-- 6. Find the maximum and minimum driver ratings for Prime Sedan bookings:
SELECT
	MAX(DRIVER_RATINGS),
	MIN(DRIVER_RATINGS)
FROM
	AHMEDABAD_RIDES
WHERE
	VEHICLE_TYPE = 'Prime Sedan';

-- 7. Find the average customer rating per vehicle type:
SELECT
	VEHICLE_TYPE,
	AVG(CUSTOMER_RATING) AS AVG_RATING
FROM
	AHMEDABAD_RIDES
GROUP BY
	VEHICLE_TYPE;
-- 8. Calculate the total booking value of rides completed successfully:
SELECT
	SUM(BOOKING_VALUE) AS TOTAL_REVENUE
FROM
	AHMEDABAD_RIDES
WHERE
	BOOKING_STATUS = 'Success';
-- 9. List all incomplete rides along with the reason:
SELECT
	BOOKING_ID,
	INCOMPLETE_RIDES_REASON
FROM
	AHMEDABAD_RIDES
WHERE
	BOOKING_STATUS = 'Incomplete';

