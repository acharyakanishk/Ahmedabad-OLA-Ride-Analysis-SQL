-- ⚠ 13. Incomplete Ride Analysis

-- Find top reasons for incomplete rides.
SELECT
	INCOMPLETE_RIDES_REASON,
	COUNT(*) AS TOTAL_INCOMPLETE_RIDES
FROM
	AHMEDABAD_RIDES
WHERE
	INCOMPLETE_RIDES = 1
GROUP BY
	INCOMPLETE_RIDES_REASON
ORDER BY
	TOTAL_INCOMPLETE_RIDES DESC;

